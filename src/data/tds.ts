export const getUniqueConstituencies = (): string[] => {
  const constituencies = TDS.map(td => td.constituency);
  return Array.from(new Set(constituencies)).sort();
};

export const getTDsByConstituency = (constituency: string): TD[] => {
  return TDS.filter(td => td.constituency === constituency);
};
